package com.box.framework.algriothm;

public class PaibanResult {
    private int x;//x边的长度
    private int y;//y边的长度
    private int m;//x边方向的个数
    private int n;//y边方向的个数
    private double p;//利用率
    private String xid;
    private int t;
    private int g;
    
	
	public int getT() {
		return t;
	}
	public void setT(int t) {
		this.t = t;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
	public String getXid() {
		return xid;
	}
	public void setXid(String xid) {
		this.xid = xid;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public double getP() {
		return p;
	}
	public void setP(double p) {
		this.p = p;
	}
    
    
}
